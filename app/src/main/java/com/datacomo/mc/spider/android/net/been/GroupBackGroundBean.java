package com.datacomo.mc.spider.android.net.been;

public class GroupBackGroundBean {

	/**
	 * 背景编号
	 */
	private int bgId;

	/**
	 * 圈子编号，包含开放主页
	 */
	private int groupId;

	/**
	 * 图片名字
	 */
	private String bgName;

	/**
	 * 图片前缀
	 */
	private String bgUrl;

	/**
	 * 图片路径
	 */
	private String bgPath;

	/**
	 * 上传本地路径
	 */
	private String bgLocalPath;

	/**
	 * 图片大小
	 */
	private String bgSize;

	/**
	 * 背景图片描述
	 */
	private String bgDesc;

	/**
	 * 上传方式
	 */
	private String uploadWay;

	/**
	 * 上传时间
	 */
	private String uploadTime;

	/**
	 * 修改时间
	 */
	private String updateTime;

	public int getBgId() {
		return bgId;
	}

	public void setBgId(int bgId) {
		this.bgId = bgId;
	}

	public int getGroupId() {
		return groupId;
	}

	public void setGroupId(int groupId) {
		this.groupId = groupId;
	}

	public String getBgName() {
		return bgName;
	}

	public void setBgName(String bgName) {
		this.bgName = bgName;
	}

	public String getBgUrl() {
		return bgUrl;
	}

	public void setBgUrl(String bgUrl) {
		this.bgUrl = bgUrl;
	}

	public String getBgPath() {
		return bgPath;
	}

	public void setBgPath(String bgPath) {
		this.bgPath = bgPath;
	}

	public String getBgLocalPath() {
		return bgLocalPath;
	}

	public void setBgLocalPath(String bgLocalPath) {
		this.bgLocalPath = bgLocalPath;
	}

	public String getBgSize() {
		return bgSize;
	}

	public void setBgSize(String bgSize) {
		this.bgSize = bgSize;
	}

	public String getBgDesc() {
		return bgDesc;
	}

	public void setBgDesc(String bgDesc) {
		this.bgDesc = bgDesc;
	}

	public String getUploadWay() {
		return uploadWay;
	}

	public void setUploadWay(String uploadWay) {
		this.uploadWay = uploadWay;
	}

	public String getUploadTime() {
		return uploadTime;
	}

	public void setUploadTime(String uploadTime) {
		this.uploadTime = uploadTime;
	}

	public String getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
}
