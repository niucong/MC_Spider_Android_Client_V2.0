package com.datacomo.mc.spider.android.net.been;

public class SingleInfoBean {

	private int uid;
	private String loginVisitWay;
	private String deviceToken;
	private String updataTime;

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public String getLoginVisitWay() {
		return loginVisitWay;
	}

	public void setLoginVisitWay(String loginVisitWay) {
		this.loginVisitWay = loginVisitWay;
	}

	public String getDeviceToken() {
		return deviceToken;
	}

	public void setDeviceToken(String deviceToken) {
		this.deviceToken = deviceToken;
	}

	public String getUpdataTime() {
		return updataTime;
	}

	public void setUpdataTime(String updataTime) {
		this.updataTime = updataTime;
	}
}
