package com.datacomo.mc.spider.android.bean;

public class CommentBeen {
	private String memberHead_Path;
	private String memberName;
	private String commentInfo;
	private String time;
	private String from;

	public String getMemberHead_Path() {
		return memberHead_Path;
	}

	public void setMemberHead_Path(String memberHead_Path) {
		this.memberHead_Path = memberHead_Path;
	}

	public String getMemberName() {
		return memberName;
	}

	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}

	public String getCommentInfo() {
		return commentInfo;
	}

	public void setCommentInfo(String commentInfo) {
		this.commentInfo = commentInfo;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

}
